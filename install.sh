cat > "$INSTALL_DIR/fix_tab.py" << CFGEOF
#!/usr/bin/env python3
"""Recreates the AD User Info tab in SOAR. Run if the tab is missing or broken."""
import json, ssl, uuid, urllib.request

SOAR_HOST  = "$SOAR_HOST"
SOAR_EMAIL = "$SOAR_EMAIL"
SOAR_PASS  = "$SOAR_PASS"
SOAR_ORG   = $SOAR_ORG
CFGEOF

cat >> "$INSTALL_DIR/fix_tab.py" << 'PYEOF'
SSL_CTX = ssl.create_default_context()
SSL_CTX.check_hostname = False
SSL_CTX.verify_mode = ssl.CERT_NONE

jar    = urllib.request.HTTPCookieProcessor()
opener = urllib.request.build_opener(
    urllib.request.HTTPSHandler(context=SSL_CTX), jar)
base = "https://" + SOAR_HOST

body = json.dumps({"email": SOAR_EMAIL, "password": SOAR_PASS}).encode()
req  = urllib.request.Request(base + "/rest/session", data=body,
       headers={"Content-Type": "application/json"}, method="POST")
with opener.open(req, timeout=15) as r:
    sess = json.loads(r.read())
csrf = sess["csrf_token"]
print("[OK] SOAR login")

def api(method, path, body=None):
    data = json.dumps(body).encode() if body is not None else None
    req  = urllib.request.Request(base + path, data=data, method=method,
           headers={"Content-Type":"application/json",
                    "Accept":"application/json","X-sess-id":csrf})
    with opener.open(req, timeout=15) as r:
        return json.loads(r.read())

layout  = api("GET", "/rest/orgs/%d/layouts/3" % SOAR_ORG)
content = layout.get("content", [])
content = [t for t in content if t.get("step_label") != "AD User Info"]

def fe(name):
    return {"step_label":None,"show_if":None,"element":"field",
            "field_type":"incident","content":name,"show_link_header":False}

ad_tab = {
    "step_label": "AD User Info",
    "element":    "tab",
    "field_type": "incident",
    "predefined_uuid": str(uuid.uuid4()),
    "show_if": [],
    "show_link_header": False,
    "fields": [
        {"step_label":None,"show_if":None,"element":"header",
         "field_type":None,"content":"Active Directory User Details",
         "show_link_header":False},
        fe("ad_username"), fe("ad_display_name"), fe("ad_email"),
        fe("ad_department"), fe("ad_title"), fe("ad_manager"),
        fe("ad_account_status"), fe("ad_last_logon"),
        fe("ad_source_ip"), fe("ad_groups"),
    ]
}
content.append(ad_tab)
layout["content"] = content
res = api("PUT", "/rest/orgs/%d/layouts/3" % SOAR_ORG, layout)
if res.get("id"):
    print("[OK] Tab 'AD User Info' recreated. Refresh SOAR in browser.")
else:
    print("[FAIL]", str(res)[:200])
PYEOF

chmod +x "$INSTALL_DIR/fix_tab.py"
echo "[OK] fix_tab.py written"

# ── 8. Create systemd service ─────────────────────────────────────────────────
echo "[...] Creating systemd service..."
cat > /etc/systemd/system/soar-ad-enrichment.service << 'SVCEOF'
[Unit]
Description=SOAR Active Directory Enrichment Service
After=network.target

[Service]
Type=simple
User=root
ExecStart=/usr/bin/python3 /opt/soar-ad-enrichment/enrichment.py
Restart=always
RestartSec=10
StandardOutput=null
StandardError=null

[Install]
WantedBy=multi-user.target
SVCEOF

systemctl daemon-reload
systemctl enable soar-ad-enrichment
systemctl restart soar-ad-enrichment
sleep 3

# ── 9. Final status ───────────────────────────────────────────────────────────
STATUS=$(systemctl is-active soar-ad-enrichment)
echo ""
echo "============================================="
if [ "$STATUS" = "active" ]; then
  echo "  Installation complete!"
else
  echo "  [WARNING] Service did not start cleanly."
  echo "  Check: journalctl -u soar-ad-enrichment -n 30"
fi
echo "============================================="
echo ""
echo "  Files:"
echo "    $INSTALL_DIR/enrichment.py    — main service"
echo "    $INSTALL_DIR/fix_tab.py       — recreate AD User Info tab"
echo "    $INSTALL_DIR/enrichment.log   — service log"
echo ""
echo "  Service control:"
echo "    systemctl status  soar-ad-enrichment"
echo "    systemctl restart soar-ad-enrichment"
echo "    tail -f $INSTALL_DIR/enrichment.log"
echo ""
echo "  If AD User Info tab is missing:"
echo "    python3 $INSTALL_DIR/fix_tab.py"
echo ""
