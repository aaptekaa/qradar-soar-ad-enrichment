class SIEMClient:
    def __init__(self):
        import base64
        creds = base64.b64encode(f"{SIEM_USER}:{SIEM_PASS}".encode()).decode()
        self.headers = {"Authorization": f"Basic {creds}",
                        "Accept": "application/json", "Version": "16.0"}
        self.base = f"https://{SIEM_HOST}"

    def _get(self, path):
        req = urllib.request.Request(self.base + path, headers=self.headers)
        try:
            with urllib.request.urlopen(req, context=SSL_CTX, timeout=15) as r:
                return json.loads(r.read())
        except Exception as e:
            log.error("SIEM GET %s failed: %s", path, e)
            return {}

    def get_source_addresses(self, ids):
        results = []
        for sid in ids[:3]:
            d = self._get(f"/api/siem/source_addresses/{sid}")
            if d.get("source_ip"):
                results.append(d["source_ip"])
        return results

    def get_offense(self, offense_id):
        return self._get(f"/api/siem/offenses/{offense_id}")


