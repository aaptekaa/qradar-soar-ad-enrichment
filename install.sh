        f"<table style='border-collapse:collapse;width:100%;'>{rows_html}</table></div>"
    )


def enrich_incident(soar, siem, incident):
    inc_id    = incident["id"]
    qradar_id = incident.get("properties", {}).get("qradar_id")
    if not qradar_id:
        return

    inc_name  = incident.get("name", "")
    username  = extract_username(inc_name)

    offense   = siem.get_offense(qradar_id)
    src_ids   = offense.get("source_address_ids", []) if offense else []
    src_ips   = siem.get_source_addresses(src_ids)

    log.info("Incident %s | offense %s | username=%s | ips=%s",
             inc_id, qradar_id, username, src_ips)

    if not username:
        if src_ips:
            soar.add_note(inc_id, f"<b>[AD Enrichment]</b> Username not found in incident name. "
                                   f"Source IPs: {', '.join(src_ips)}")
        return

    ad_info = query_ad_object(username)

    full_inc = soar.get_incident(inc_id)
    if not full_inc:
        return

    if not ad_info:
        full_inc["properties"].update({
            "ad_username":       username,
            "ad_display_name":   "[Not found in AD]",
            "ad_account_status": "NOT FOUND IN AD",
            "ad_source_ip":      ", ".join(src_ips),
        })
        soar.update_incident(inc_id, full_inc)
        soar.add_note(inc_id,
            f"<b>[AD Enrichment]</b> User <b>{username}</b> not found in Active Directory. "
            f"Source IPs: {', '.join(src_ips) if src_ips else 'N/A'}")
        return

    full_inc["properties"].update({
        "ad_username":       ad_info.get("username", username),
        "ad_display_name":   ad_info.get("display_name", ""),
        "ad_email":          ad_info.get("email", ""),
        "ad_department":     ad_info.get("department", ""),
        "ad_title":          ad_info.get("title", ""),
        "ad_manager":        ad_info.get("manager", ""),
        "ad_account_status": ad_info.get("account_status", ""),
        "ad_last_logon":     ad_info.get("last_logon", ""),
        "ad_source_ip":      ", ".join(src_ips),
        "ad_groups":         ad_info.get("groups", ""),
    })
    soar.update_incident(inc_id, full_inc)
    soar.add_note(inc_id, build_note_html(username, ad_info, src_ips, qradar_id))
    log.info("Incident %s enriched with AD info for user %s", inc_id, username)


# ── State ─────────────────────────────────────────────────────────────────────

def load_state():
    try:
        with open(STATE_FILE) as f:
            return set(json.load(f))
    except Exception:
        return set()

def save_state(processed):
    with open(STATE_FILE, "w") as f:
        json.dump(list(processed), f)


# ── Main loop ─────────────────────────────────────────────────────────────────

def main():
    log.info("=== SOAR AD Enrichment Service starting ===")
    soar      = SOARClient()
    siem      = SIEMClient()
    processed = load_state()

    while True:
        try:
            incidents = soar.get_incidents()
            if isinstance(incidents, list):
                for inc in incidents:
                    inc_id    = str(inc.get("id"))
                    qradar_id = inc.get("properties", {}).get("qradar_id")
                    if inc_id not in processed and qradar_id:
                        try:
                            enrich_incident(soar, siem, inc)
                        except Exception as e:
                            log.error("Error enriching incident %s: %s", inc_id, e)
                        processed.add(inc_id)
                        save_state(processed)
        except Exception as e:
            log.error("Main loop error: %s", e)
            try:
                soar = SOARClient()
            except Exception:
                pass
        time.sleep(POLL_INTERVAL)

if __name__ == "__main__":
    main()
